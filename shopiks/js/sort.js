function sortByPrice(products) {
    return products.sort((a, b) => a.price - b.price);
}

function sortByPrice2(products) {
    return products.sort((a, b) => b.price - a.price);
}

function Sort1 () {
    let sortedProducts = sortByPrice(products);


    // Очистить контейнер
    document.getElementById("content2").innerHTML = "";

    // Добавить отсортированные карточки
    sortedProducts.forEach(product => {
        const card = 
        "<div class='row3-items-container product' id='product-id-" + product.id + "'>" +
        "<div class='row3-item-information-container'>" +
            "<img class='product-img' src='" + product.img +"' alt=''>" +
            "<div class='add-to-cart-more'>" +
                "<div class='more'>" +
                "<a href='details.html'>" +
                    "<i class='fa-solid fa-info'></i>" +
                "</a>" +
                "</div>" +
                "<div class='add-to-cart' onclick='addToCart(" + product.id + ")' id='" + product.id + "'>" +
                    "<i class='fa-solid fa-cart-plus add-item-to-cart'></i>" +
                "</div>" +
            "</div>" +
        "</div>" +
        "<div class='name-price'>" +
            "<p class='item-name'>" + product.name +"</p><span class='item-price'>$ " + product.price + "</span>" +
        "</div>" +
    "</div>";

        // Добавить созданную карточку на страницу
        document.getElementById("content2").insertAdjacentHTML('beforeend', card);
    });
}


